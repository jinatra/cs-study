# ================================================================
# 문제 설명
# ================================================================
# 프로그래머스 치킨은 치킨을 시켜먹으면 한 마리당 쿠폰을 한 장 발급합니다.
# 쿠폰을 열 장 모으면 치킨을 한 마리 서비스로 받을 수 있고,
# 서비스 치킨에도 쿠폰이 발급됩니다.
# 시켜먹은 치킨의 수 chicken이 매개변수로 주어질 때
# 받을 수 있는 최대 서비스 치킨의 수를 return하세요.
#
# ================================================================
# 제한사항
# ================================================================
# - chicken은 정수입니다.
# - 0 ≤ chicken ≤ 1,000,000
#
# ================================================================
# 입출력 예
# ================================================================
# chicken | result
# 100     | 11
# 1081    | 120
#
# ================================================================
# 입출력 예 설명
# ================================================================
# 예 #1
#   100마리 주문 → 쿠폰 100장 → 서비스 10마리
#   10마리 주문  → 쿠폰 10장  → 서비스 1마리
#   총 10 + 1 = 11 return
#
# 예 #2
#   1081마리 주문 → 쿠폰 1081장 → 서비스 108마리, 쿠폰 1장 남음
#   108마리 주문  → 쿠폰 108장  → 서비스 10마리,  쿠폰 8장 남음
#   10마리 주문   → 쿠폰 10장   → 서비스 1마리
#   1마리 주문    → 쿠폰 1장 발급
#   남은 쿠폰 합산 → 총 10장 → 서비스 1마리 추가
#   총 108 + 10 + 1 + 1 = 120 return
# ================================================================


def solution(chicken):
    coupon = chicken  # 현재 가진 쿠폰 수
    service = 0 # 받은 서비스 치킨 수
    while coupon >= 10:
        new_chicken = coupon // 10 # 서비스 치킨
        coupon = coupon % 10 + new_chicken #남은쿠폰 + 서비스쿠폰
        service += new_chicken
    return service